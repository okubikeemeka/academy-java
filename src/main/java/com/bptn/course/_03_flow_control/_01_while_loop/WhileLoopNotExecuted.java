package com.bptn.course._03_flow_control._01_while_loop;

public class WhileLoopNotExecuted {

	public static void main(String[] args) {
		
		        int i = 6;  // Initialize the counter to a value greater than 5

		        // While loop that will not execute
		        System.out.println("While Loop (Not Executed):");
		        while (i <= 5) {
		            System.out.println(i);  // This line will not be executed
		            i++;  // This line will not be executed
		        }

		        System.out.println("The while loop did not execute.");
		    }
		}
	
		
		
		
	

